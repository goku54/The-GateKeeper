    private void submit_accMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit_accMouseClicked
        // TODO add your handling code here:
          if(txt_Name.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please enter the name of the user ","Update Access Privilege",2);
        }
        else
        {
              try {
                  //query to take the name to the db and retrieve its access type in the nxt txtbox
                  String sql="select * from accdetails where username=? ";
                  ps=con.prepareStatement(sql);
                  ps.setString(1, txt_Name.getText());
                  rs=ps.executeQuery();
                  if(rs.next())
                  {
                        String add1 = rs.getString("accestype");
                        txt_accessType.setText(add1);
                        submit_acc.setEnabled(false);
                  }
                      txt_Name.enable(false);
                  acc_type=txt_accessType.getText();
              } catch (SQLException ex) {
                  Logger.getLogger(UpgradeAccGUI.class.getName()).log(Level.SEVERE, null, ex);
              }
        }
    }//GEN-LAST:event_submit_accMouseClicked

