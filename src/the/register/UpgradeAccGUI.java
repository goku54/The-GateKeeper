    private void UpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateMouseClicked
        // TODO add your handling code here:
           if(s.equals(acc_type))
        {
            JOptionPane.showMessageDialog(null,"Same access type has been assigned","Update Access-Type" ,2);
         int choice=   JOptionPane.showConfirmDialog(null,"Dont you want to change your access type ?","Update Access-Type",JOptionPane.YES_NO_OPTION,3, null);
        
         if(choice==1)
         {
             ManageAccGUI m = new ManageAccGUI();
                close();
                m.setSize(438, 310);
                m.setLocation(470,180);
                m.setVisible(true);
         }
        else
         {
             Update.setEnabled(false);
             submit_acc.setEnabled(true);
         }
        }
           else
           {
               choice = JOptionPane.showConfirmDialog(null,"Have you made the right choice ?","Upgrate Access-Type",JOptionPane.YES_NO_OPTION,3, null);
               if(choice==0)
               {
                   try {
                       //query to update the access
                       String value=Access_typeBox.getSelectedItem().toString();
                       String uname=txt_Name.getText();
                       String sql="update accdetails set accestype='"+value+"' where username='"+uname+"'";
                       ps=con.prepareStatement(sql);
                       
                     
                       
                       ps.execute();
                       JOptionPane.showMessageDialog(null,"Account Access-Type upgraded","Upgrade Access_Type",1);
                       Update.setEnabled(false);
                       ManageAccGUI m = new ManageAccGUI();
                       m.setLocation(470,180);
                       m.setSize(438, 310);
                       m.setVisible(true);
                       close();
                   } catch (SQLException ex) {
                       Logger.getLogger(UpgradeAccGUI.class.getName()).log(Level.SEVERE, null, ex);
                   }
                   
               }
               else
               {
                   Update.setEnabled(true);
               }
           }
    }//GEN-LAST:event_UpdateMouseClicked

