    private void txt_passwordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_passwordKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== KeyEvent.VK_ENTER)
        {
            try {
                String sql="Select * from accdetails where username=? and password=?";
                ps=con.prepareStatement(sql);
                ps.setString(1,txt_username.getText() );
                ps.setString(2,txt_password.getText() );
                
                
                rs=ps.executeQuery();
                
                if(rs.next())
                {
                    try {
                        String add2 = rs.getString("accestype");
                        getacctype.setText(add2);
                        MainGUI m1 = new MainGUI();
                        m1.getAccess(add2);
                        
                        
                        JOptionPane.showMessageDialog(null,"Login Successful","Authentication",1);
                      
                        m1.setVisible(true);
                        close();
                        rs.close();
                        ps.close();
                        
                        
                        m1.user_nameRecieve(txt_username.getText());
                    } catch (SQLException ex) {
                        Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else
            {
                JOptionPane.showMessageDialog(null,"Invalid Username or Password ","Authentication",0);
            }
            } catch (SQLException ex) {
                Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txt_passwordKeyPressed
 
