    private void log_inMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_log_inMouseClicked
        try {
            // TODO add your handling code here:
            String sql="Select * from accdetails where username=? and password=?";
            ps=con.prepareStatement(sql);
            ps.setString(1,txt_username.getText() );
            ps.setString(2,txt_password.getText() );
           
                    
            rs=ps.executeQuery();
            
            if(rs.next())
            {
                String add2 = rs.getString("accestype");
               getacctype.setText(add2);
               
               if(add2.equals("Receptionist"))
               {
                   ManageAccGUI m1 = new ManageAccGUI();
                   m1.getAccess(add2);
               }
                MainGUI m1 = new MainGUI();
                m1.getAccess(add2);
                
                
                JOptionPane.showMessageDialog(null,"Login Successful","Authentication",1);
            
            
                 
                  m1.user_nameRecieve(txt_username.getText());
              m1.setVisible(true);
             
             if(visible==false)
               {
                  this.dispose();
                   this.setVisible(false);
                   close();
                   //m1.setVisible(true);
               }
              
            }
            else if(txt_username.getText().equals("")||txt_password.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null,"Please enter the Username and Password ","Authentication",2);
              
            }
            
            else
            {
                JOptionPane.showMessageDialog(null,"Invalid Username or Password ","Authentication",0);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
                    {
                        
            try {
                rs.close();
                ps.close();
            } catch (SQLException ex) {
                Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
                    }       
    }//GEN-LAST:event_log_inMouseClicked

